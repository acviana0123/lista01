package exercicio2;

public class Tabuada {
    public void soma(float nro1, float nro2){
        System.out.println("Soma: "+(nro1+nro2));
    }

    public void subtracao(float nro1, float nro2){
        System.out.println("Subtracao: "+(nro1-nro2));
    }
    
    public void multiplicacao(float nro1, float nro2){
        System.out.println("Multiplicacao: "+(nro1*nro2));
    }
    public void divisao(float nro1, float nro2){
        System.out.println("Divisao: "+(nro1/nro2));
    }

}
